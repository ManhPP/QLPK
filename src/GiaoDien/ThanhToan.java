    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        ConnectionDB db = new ConnectionDB();
        db.connect();

        //hien thong tin len bang dich vuva bang thuoc
        if (jTextField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Không được để trống id", "Thông báo", 0);
        } else {
            int idct = Integer.parseInt(jTextField1.getText());
            int tongTien = 0;

            ChiTietKhamBenh ct = db.timCT(idct);
            if (ct == null) {
                JOptionPane.showMessageDialog(null, "Không tồn tại chi tiết khám bệnh này!!!", "Thông báo", 0);
            }
            Date ngayKham = ct.getNgayKham();
            DateFormat df = new SimpleDateFormat("MM/dd/yyyy");
            String text = df.format(ngayKham);
            jTextField5.setText(text);

            ArrayList<DichVu> dsdv = db.dsDichVu(idct);
//            System.out.println(dsdv.size());
            if (dsdv == null) {
                JOptionPane.showMessageDialog(null, "Khong tim thay dich vu nao!");
            } else {
                DefaultTableModel model1 = (DefaultTableModel) jTable1.getModel();
                model1.setRowCount(0);

                Object[] col = new Object[2];

                for (int i = 0; i < dsdv.size(); i++) {
                    col[0] = dsdv.get(i).getTen();
                    col[1] = dsdv.get(i).getGia();
                    model1.addRow(col);
                    tongTien += dsdv.get(i).getGia();
                }
            }

            ArrayList<Thuoc> dsThuoc = db.dsThuoc(idct);
            if (dsThuoc == null) {
                JOptionPane.showMessageDialog(null, "Khong tim thay thuoc nao!");
            } else {

                DefaultTableModel model2 = (DefaultTableModel) jTable2.getModel();
                model2.setRowCount(0);

                Object[] col = new Object[4];

                for (int i = 0; i < dsThuoc.size(); i++) {
                    col[0] = dsThuoc.get(i).getTenThuoc();
                    col[1] = dsThuoc.get(i).getSoLuong();
                    col[2] = dsThuoc.get(i).getDonGia();
                    if (dsThuoc.get(i).getSoLuongConLai() >= dsThuoc.get(i).getSoLuong()) {
                        col[3] = "ok";
                        tongTien += dsThuoc.get(i).getSoLuong() * dsThuoc.get(i).getDonGia();
                    } else {
                        col[3] = "khong du thuoc";
                    }

                    model2.addRow(col);
                }
            }

            jTextField3.setText(String.valueOf(tongTien));
        }
    }//GEN-LAST:event_jButton4ActionPerformed

