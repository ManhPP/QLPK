    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        ConnectionDB db = new ConnectionDB();
        db.connect();

        Document file = new Document();

        //cap nhat so luong thuoc
        if (jTextField1.getText().isEmpty()); else {
            int idct = Integer.parseInt(jTextField1.getText());

            ChiTietKhamBenh ct = db.timCT(idct);

            ArrayList<Thuoc> dsThuoc = db.dsThuoc(idct);
            if (dsThuoc == null) {
                JOptionPane.showMessageDialog(null, "Khong tim thay thuoc nao!");
            } else {

                try {
                    // font viet tieng viet
                    // font arial dam
                    String path = "Unicode\\arialbd.ttf";
                    FontFactory.register(path);
                    Font bold = new Font(BaseFont.createFont(path, BaseFont.IDENTITY_H, BaseFont.EMBEDDED));
                    // font arial binh thuong
                    path = "Unicode\\arial.ttf";
                    FontFactory.register(path);
                    Font arial = new Font(BaseFont.createFont(path, BaseFont.IDENTITY_H, BaseFont.EMBEDDED));
                    // font arial nghieng
                    path = "Unicode\\ariali.ttf";
                    FontFactory.register(path);
                    Font italic = new Font(BaseFont.createFont(path, BaseFont.IDENTITY_H, BaseFont.EMBEDDED));

                    String nameFile = "don_" + idct + ".pdf";
                    PdfWriter.getInstance(file, new FileOutputStream(nameFile));
                    file.open();
//                    Font bold = new Font(Font.FontFamily.TIMES_ROMAN, 16, Font.BOLD);
//                    Font italic = new Font(Font.FontFamily.TIMES_ROMAN, 12, Font.ITALIC);
                    Paragraph pr1 = new Paragraph("PHÒNG KHÁM ĐA KHOA QUANG TRUNG", bold);
                    Paragraph pr2 = new Paragraph("Phiếu Thu Phí Khám Bệnh",arial);
                    pr1.setAlignment(Element.ALIGN_CENTER);
                    pr1.setSpacingAfter(20);
                    pr2.setAlignment(Element.ALIGN_CENTER);
                    pr2.setSpacingAfter(20);
                    Paragraph pr3 = new Paragraph("                 Tên người mua: " + db.timTen(idct) + "                                                                                ID: " + idct,arial);
                    pr3.setSpacingAfter(20);
                    file.add(pr1);
                    file.add(pr2);
                    file.add(pr3);

                    Paragraph pr4 = new Paragraph("                 1. Phí dịch vụ: ", italic);
                    pr4.setSpacingAfter(15);
                    file.add(pr4);

                    PdfPTable table = new PdfPTable(2);

                    PdfPCell header1 = new PdfPCell(new Paragraph("                     Tên dịch vụ", arial));
                    header1.setPadding(5);
                    PdfPCell header2 = new PdfPCell(new Paragraph("                     Giá dịch vụ", arial));
                    header2.setPadding(5);
                    table.addCell(header1);
                    table.addCell(header2);

                    PdfPCell cell;
                    DefaultTableModel model1 = (DefaultTableModel) jTable1.getModel();

                    for (int i = 0; i < model1.getRowCount(); i++) {
                        for (int j = 0; j < model1.getColumnCount(); j++) {
                            cell = new PdfPCell(new Paragraph(model1.getValueAt(i, j).toString(),arial));
                            cell.setPadding(5);
                            table.addCell(cell);
                        }
                    }

                    file.add(table);

                    Paragraph pr5 = new Paragraph("                 2. Phí mua thuốc: ", italic);
                    pr5.setSpacingBefore(15);
                    pr5.setSpacingAfter(15);

                    file.add(pr5);

                    table = new PdfPTable(4);

                    header1 = new PdfPCell(new Paragraph("Tên thuốc"));
                    header1.setPadding(5);
                    header2 = new PdfPCell(new Paragraph("Số lượng", arial));
                    header2.setPadding(5);
                    PdfPCell header3 = new PdfPCell(new Paragraph("Đơn giá",arial));
                    header3.setPadding(5);
                    PdfPCell header4 = new PdfPCell(new Paragraph("Thành tiền", arial));
                    header4.setPadding(5);
                    table.addCell(header1);
                    table.addCell(header2);
                    table.addCell(header3);
                    table.addCell(header4);

                    DefaultTableModel model2 = (DefaultTableModel) jTable2.getModel();

                    for (int i = 0; i < model2.getRowCount(); i++) {
                        for (int j = 0; j < model2.getColumnCount() - 1; j++) {
                            cell = new PdfPCell(new Paragraph(model2.getValueAt(i, j).toString(), arial));
                            cell.setPadding(5);
                            table.addCell(cell);
                        }

                        cell = new PdfPCell(new Paragraph(Integer.toString((Integer.parseInt(model2.getValueAt(i, 2).toString()) * Integer.parseInt(model2.getValueAt(i, 1).toString()))),arial));
                        table.addCell(cell);
                    }

                    file.add(table);

                    bold.setSize(14);
                    Paragraph pr6 = new Paragraph("                 Tổng tiền: " + jTextField3.getText()+" VND", bold);
                    pr6.setSpacingBefore(15);
                    pr6.setSpacingAfter(15);
                    file.add(pr6);

                    DateFormat df = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss                   ");

                    Date today = new Date(System.currentTimeMillis());
                    String reportDate = df.format(today);
                    Paragraph pr7 = new Paragraph(reportDate);
                    pr7.setAlignment(Element.ALIGN_RIGHT);
                    pr7.setSpacingAfter(15);
                    file.add(pr7);

                    Paragraph pr8 = new Paragraph("                 Chứ ký người mua:                                                                     Chữ ký dược sĩ",arial);
                    file.add(pr8);

                    file.close();
                } catch (DocumentException e) {
                    e.printStackTrace();
                } catch (FileNotFoundException e) {
                    e.printStackTrace();
                } catch (IOException ex) {
                    Logger.getLogger(ThanhToan.class.getName()).log(Level.SEVERE, null, ex);
                }

                for (int i = 0; i < dsThuoc.size(); i++) {
                    if (dsThuoc.get(i).getSoLuongConLai() >= dsThuoc.get(i).getSoLuong()) {
                        db.updateThuoc(dsThuoc.get(i).getId(), dsThuoc.get(i).getSoLuongConLai() - dsThuoc.get(i).getSoLuong());
                    }
                }
                JOptionPane.showMessageDialog(null, "Thành công!!!", "Thông báo", 1);
            }

        }

    }//GEN-LAST:event_jButton1ActionPerformed

