        arialb.setSize(20);
        c.setFont(arialb);
        p = new Paragraph();
        p.add(c);
        p.setAlignment(Element.ALIGN_CENTER);
        p.setSpacingAfter(20);
        p.setSpacingBefore(30);
        document.add(p);

        // thong tin benh nhan
