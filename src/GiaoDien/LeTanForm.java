        SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");
        String ngay = format.format(time);
        c = new Chunk("Ngày: " + ngay);
        c.setFont(arialb);
        p = new Paragraph();
        p.add(c);
        document.add(p);

        // tittle
