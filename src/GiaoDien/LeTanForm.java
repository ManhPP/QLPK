    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            ConnectionDB db = new ConnectionDB();
            db.connect();
            //bệnh nhân mới
            BenhNhan bnmoi = new BenhNhan();
            bnmoi.setTen(txtHoTen.getText());
            bnmoi.setGioiTinh(txtGioiTinh.getText());
            Date date = new Date(new SimpleDateFormat("dd/MM/yyyy").parse(txtNgaySinh.getText()).getTime());
            bnmoi.setNgaySinh(date);
            bnmoi.setDiaChi(txtDiaChi.getText());
            bnmoi.setSDT(txtSdt.getText());
            bnmoi.setCMT(txtCmt.getText());
            BenhNhan bncu = new BenhNhan();
            bncu = db.themBenhNhan(bnmoi.getTen(), bnmoi.getGioiTinh(), bnmoi.getNgaySinh(), bnmoi.getDiaChi(), bnmoi.getSDT(), bnmoi.getCMT());
            if (bncu == null) {
                int id = db.getLastID();
                bnmoi.setID(id);
                inPhieu(bnmoi);
                //in phiếu với các thông tin đã nhập của bnmoi
            } else {
                //in phiếu với các thông tin của bncu
                if (bnmoi.getTen().equals(bncu.getTen()) && bnmoi.getGioiTinh().equals(bncu.getGioiTinh())
                        && bnmoi.getDiaChi().equals(bncu.getDiaChi()) && bnmoi.getSDT().equals(bncu.getSDT())
                        && bnmoi.getNgaySinh().getTime() == bncu.getNgaySinh().getTime()
                        ) {
                    JOptionPane.showMessageDialog(null, "Bệnh nhân đã tồn tại!! Chỉ in phiếu thứ tự!!", "Thông báo", JOptionPane.WARNING_MESSAGE);
                    inPhieu(bncu);
                }else{
                    JOptionPane.showMessageDialog(null, "Số chứng minh thư đã được sử dụng!", "Thông báo", 2);
                    STT--;
                }

            }
            STT++;
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, "Kiểm tra lại ngày nhập!!!", "thông báo", 0);
        } catch (Exception ex) {

        }
    }//GEN-LAST:event_jButton1ActionPerformed

